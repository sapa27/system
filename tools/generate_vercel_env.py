    print(json.dumps({
        "ok": True,
        "output": str(OUT.relative_to(ROOT)),
        "release": cfg["releaseStamp"],
        "serverGasEnvRequired": True,
        "vercelApiProxyEnabled": True,
        "deterministicPublicEnv": True,
    }, ensure_ascii=False))


